public class WxAgent {
	private int agentid;
	private String name;
	private String square_logo_url;
	private String round_logo_url;
	/**
	 * @return the agentid
	 */
	public int getAgentid() {
		return agentid;
	}
	/**
	 * @param agentid the agentid to set
	 */
	public void setAgentid(int agentid) {
		this.agentid = agentid;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the square_logo_url
	 */
	public String getSquare_logo_url() {
		return square_logo_url;
	}
	/**
	 * @param squareLogoUrl the square_logo_url to set
	 */
	public void setSquare_logo_url(String squareLogoUrl) {
		square_logo_url = squareLogoUrl;
	}
	/**
	 * @return the round_logo_url
	 */
	public String getRound_logo_url() {
		return round_logo_url;
	}
	/**
	 * @param roundLogoUrl the round_logo_url to set
	 */
	public void setRound_logo_url(String roundLogoUrl) {
		round_logo_url = roundLogoUrl;
	}
	
	

}
