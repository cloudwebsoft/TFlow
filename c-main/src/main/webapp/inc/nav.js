function menu_table_onmouseover(tableObj) {
	tableObj.border = 1;
}

function menu_table_onmouseout(tableObj) {
	tableObj.border = 0;
}

function menu_td_onmouseover(tdObj) {
	tdObj.border = 1;
	tdObj.borderColorDark = "#848284";
	tdObj.borderColorLight = "#ffffff";
}

function menu_td_onmouseout(tdObj) {
	tdObj.border = 0;
	tdObj.borderColorDark = "";
	tdObj.borderColorLight = "";
}